class Panelim extends JPanel {
    PembeKare kýrmýzýKare = new PembeKare(); // Namevcut varsayýlý sýnýf kurucusunu çaðýrýr...

    public Panelim() {//Kurucu...
        setBorder (BorderFactory.createLineBorder (Color.RED));
        setBackground (Color.getHSBColor ((float)Math.random(), (float)Math.random(), (float)Math.random()) );

        addMouseListener (new MouseAdapter() {// Fare týklamasýna duyarlý...
            public void mousePressed (MouseEvent olay) {kareyiTaþý (olay.getX(), olay.getY());}
        }); // add.. ifadesi sonu...

        addMouseMotionListener (new MouseAdapter() {// Fare sürüklemesine duyarlý...
            public void mouseDragged (MouseEvent olay) {kareyiTaþý (olay.getX(), olay.getY());}
        }); // add.. ifadesi sonu...
    } // Panelim() kurucusu sonu...

    private void kareyiTaþý (int x, int y) {
        final int AKTÜEL_X = kýrmýzýKare.getX();
        final int AKTÜEL_Y = kýrmýzýKare.getY();
        final int AKTÜEL_EN = kýrmýzýKare.getWidth();
        final int AKTÜEL_BOY = kýrmýzýKare.getHeight();
        final int KAYMA = 1;

        if ((AKTÜEL_X != x) || (AKTÜEL_Y != y)) {
            repaint (AKTÜEL_X, AKTÜEL_Y, AKTÜEL_EN+KAYMA, AKTÜEL_BOY+KAYMA); // paintComponent(..) hazýr fonksiyonu çaðýrýrýr...
            kýrmýzýKare.setX (x);
            kýrmýzýKare.setY (y);
            repaint (kýrmýzýKare.getX(), kýrmýzýKare.getY(), kýrmýzýKare.getWidth()+KAYMA, kýrmýzýKare.getHeight()+KAYMA); // paintComponent(..) hazýr fonksiyonu çaðýrýrýr...
        } // if kararý sonu...
    } // kareyiTaþý(..) metodu sonu...

    public void paintComponent (Graphics g) {
        super.paintComponent (g);       
        g.setColor (Color.YELLOW);
        g.drawString ("Bu benim özel panelimdir!", 50, 80);
        kýrmýzýKare.kareyiBoya (g);
    } // paintComponent(..) hazýr metodu sonu...

    public Dimension getPreferredSize() {return new Dimension (250, 200);} // Hazýr metod...
} // Panelim sýnýfý sonu...

class PembeKare {// 20x20 boyutlu içi pembe, çerçevesi siyah bir kare çizer...
    private int xKonumu = 100; // ilk (x,y)=(100,30) konumu...
    private int yKonumu = 30;
    private int en = 20;
    private int boy = 20;

    public void setX (int xKonumu) {this.xKonumu = xKonumu;} // Yatay-koy...
    public int getX() {return xKonumu;} // Yatay-al...
    public void setY (int yKonumu) {this.yKonumu = yKonumu;} // Dikey-koy...
    public int getY() {return yKonumu;} // Dikey-al...
    public int getWidth() {return en;} // En-al (Koy yok, çünkü sabit (20,20)...
    public int getHeight() {return boy;} // Boy-al...

    public void kareyiBoya (Graphics g) {
        g.setColor (Color.PINK);
        g.fillRect (xKonumu, yKonumu, en, boy);
        g.setColor (Color.BLACK);
        g.drawRect (xKonumu, yKonumu, en, boy);  
    } // kareyiBoya(..) metodu sonu...
} // PembeKare sýnýfý sonu...