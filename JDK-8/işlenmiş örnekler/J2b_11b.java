class J2b_11b {
    public static void main (String[] args) {
        int say = 1;

        while (say < 11) {
            System.out.println ("while döngü tekrarý: " + say);
            say++;
        } // while döngüsü sonu...
    } // main(..) metodu sonu...
} // J2b_11b sýnýfý sonu...

